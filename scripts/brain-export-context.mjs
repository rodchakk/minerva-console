#!/usr/bin/env node
// scripts/brain-export-context.mjs
//
// Export a single, copy-pastable Markdown snapshot of the Git-backed Brain so it
// can be handed to ChatGPT / Claude / Codex / Gemini as context.
//
// Pure Node.js (ESM), zero dependencies. Reads only safe Brain content under
// content/brain/**. It never reads .env*, .claude/**, node_modules, .git, .next,
// features/entry/**, or any secret, and it never imports app code. This is not
// RAG, embeddings, a database, or an agent engine — just a deterministic file
// concatenation.
//
// Run: node scripts/brain-export-context.mjs
//
// Output: content/brain/exports/brain-context.md
//
// The export is ordered alphabetically by relative path and carries no dynamic
// timestamp, so re-running it produces no diff unless Brain content changed.

import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from "fs";
import { join, relative, sep } from "path";
import { fileURLToPath } from "url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const ROOT = join(__dirname, "..");
const BRAIN_DIR = join(ROOT, "content", "brain");
const REGISTRY_DIR = join(BRAIN_DIR, "registries");
const EXPORTS_DIR = join(BRAIN_DIR, "exports");
const OUTPUT_FILE = join(EXPORTS_DIR, "brain-context.md");

// ─── ANSI colours ────────────────────────────────────────────────────────────
const GRN = "\x1b[32m";
const BOLD = "\x1b[1m";
const RST = "\x1b[0m";

// Directory name (relative to the repo root, POSIX-style) that must never be
// walked — the exports folder, so the export never includes itself.
const EXPORTS_REL = "content/brain/exports";

/** Repo-relative POSIX path (stable across platforms for titles and sorting). */
function relPosix(absPath) {
  return relative(ROOT, absPath).split(sep).join("/");
}

/** Recursively collect files with one of the given extensions under a dir,
 *  skipping the exports folder. Returns repo-relative POSIX paths. */
function collect(dir, extensions) {
  const out = [];
  if (!existsSync(dir)) return out;
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const abs = join(dir, entry.name);
    const rel = relPosix(abs);
    if (rel === EXPORTS_REL || rel.startsWith(`${EXPORTS_REL}/`)) continue;
    if (entry.isDirectory()) {
      out.push(...collect(abs, extensions));
    } else if (extensions.some((ext) => entry.name.endsWith(ext))) {
      out.push(rel);
    }
  }
  return out;
}

if (!existsSync(BRAIN_DIR)) {
  console.error(`Error: ${relPosix(BRAIN_DIR)} does not exist`);
  process.exit(1);
}

// ─── Gather content (stable alphabetical order) ───────────────────────────────
const registryFiles = collect(REGISTRY_DIR, [".json"]).sort();
const markdownFiles = collect(BRAIN_DIR, [".md"]).sort();

// ─── Build the export document ────────────────────────────────────────────────
const parts = [];

parts.push("# Minerva Core Brain — Context Export");
parts.push("");
parts.push(
  "A single Git-backed snapshot of Minerva Core Brain, concatenated for handoff " +
    "to an AI assistant (ChatGPT, Claude, Codex, Gemini).",
);
parts.push("");

parts.push("## How to use this file");
parts.push("");
parts.push(
  "- Paste this whole file into the assistant as background context for Brain work.",
);
parts.push(
  "- It is generated by `node scripts/brain-export-context.mjs` from `content/brain/**`. Do not hand-edit it — re-run the script instead.",
);
parts.push(
  "- It is read-only knowledge: registries (JSON) plus their linked Markdown docs. No database, RAG, embeddings, or secrets are included.",
);
parts.push(
  "- Authority still lives in Git/GitHub. This export is a convenience copy and may lag behind `master` if not regenerated.",
);
parts.push("");

// Registries index + content.
parts.push("## Registries index");
parts.push("");
if (registryFiles.length === 0) {
  parts.push("_No registry JSON files found._");
} else {
  for (const rel of registryFiles) parts.push(`- \`${rel}\``);
}
parts.push("");

parts.push("## Registries");
parts.push("");
for (const rel of registryFiles) {
  parts.push(`### ${rel}`);
  parts.push("");
  parts.push("```json");
  parts.push(readFileSync(join(ROOT, rel), "utf8").trimEnd());
  parts.push("```");
  parts.push("");
}

// Markdown docs index + content.
parts.push("## Documents index");
parts.push("");
if (markdownFiles.length === 0) {
  parts.push("_No Markdown documents found._");
} else {
  for (const rel of markdownFiles) parts.push(`- \`${rel}\``);
}
parts.push("");

parts.push("## Documents");
parts.push("");
for (const rel of markdownFiles) {
  parts.push(`### ${rel}`);
  parts.push("");
  parts.push(readFileSync(join(ROOT, rel), "utf8").trimEnd());
  parts.push("");
}

const output = `${parts.join("\n").trimEnd()}\n`;

// ─── Write ────────────────────────────────────────────────────────────────────
if (!existsSync(EXPORTS_DIR)) mkdirSync(EXPORTS_DIR, { recursive: true });
writeFileSync(OUTPUT_FILE, output, "utf8");

console.log(
  `\n${GRN}${BOLD}Brain context export${RST} — wrote ${relPosix(OUTPUT_FILE)}`,
);
console.log(`  documents included: ${markdownFiles.length}`);
console.log(`  registries included: ${registryFiles.length}\n`);
